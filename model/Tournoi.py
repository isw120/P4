class Tournoi:

    def __init__(self, Nom, Lieu, Date, Nombre_de_tours, Tournees, Joueurs, Controle_du_temps, Description, status,
                 secondRoundPlayers, thirdRoundPlayers):
        self.Nom = Nom
        self.Lieu = Lieu
        self.Date = Date
        self.Nombre_de_tours = Nombre_de_tours
        self.Tournees = Tournees
        self.Joueurs = Joueurs
        self.Controle_du_temps = Controle_du_temps
        self.Description = Description
        self.status = status
        self.secondRoundPlayers = secondRoundPlayers
        self.thirdRoundPlayers = thirdRoundPlayers
